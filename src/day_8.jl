using Combinatorics

function run()
    antinodes = []
    data = readlines("input/day_8.txt")
    data = stack(collect.(data); dims=1)
    max_i, max_j = size(data)
    min_i, min_j = 1, 1
    for char in unique(data)
        if char != '.'
            for pair in combinations(findall(x->x==char, data), 2)
                ant1, ant2 = pair
                i = abs(ant1[1] - ant2[1])
                if ant1[1] > ant2[1]
                    I1 = ant1[1] + i
                    I2 = ant2[1] - i
                elseif  ant1[1] == ant2[1]
                    I1 = I2 = ant1[1]
                elseif ant1[1] < ant2[1]
                    I1 = ant1[1] - i
                    I2 = ant2[1] + i
                end

                j = abs(ant1[2] - ant2[2])
                if ant1[2] > ant2[2]
                    J1 = ant1[2] + j
                    J2 = ant2[2] - j
                elseif  ant1[2] == ant2[2]
                    J1 = J2 = ant1[2]
                elseif ant1[2] < ant2[2]
                    J1 = ant1[2] - j
                    J2 = ant2[2] + j
                end

                if min_i <= I1 <= max_i && min_j <= J1 <= max_j
                    push!(antinodes, (I1, J1))
                end
                if min_i <= I2 <= max_i && min_j <= J2 <= max_j
                    push!(antinodes, (I2, J2))
                end
            end
        end
    end
    return length(Set(antinodes))
end

println(run())
